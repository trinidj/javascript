let a = 12;
let b = 10;

// AND Operation ( & )
console.log(a & b); //* Output: 8

// OR Operation ( | )
console.log(a | b); //* Output: 14

// XOR Operation ( ^ )
console.log(a ^ b); //* Output: 6

// NOT Operation ( ~ )
let c = 13;
console.log(~c); //* Output: -14

// Left Shift
console.log(a << 2); //* Output: 48

// Right Shift
let d = -12
console.log(d >> 2); //* Output: -3

// Zero-Fill Right Shift
console.log(d >>> 2); //* Output: 1073741821

